public class Variabel {
    public static void main(String[] args) {
        String namaDepan = "Salma";
        String namaBelakang = "Awliya Nafsani";
        int umur = 17;
        double tinggiBadan = 155;
        double beratBadan = 45.5;
        System.out.println("Nama saya " + namaDepan);
        System.out.println("Nama belakang saya " + namaBelakang);
        System.out.println("Umur saya " + (double)umur);  //mengubah nilai int ke double
        System.out.println("Tinggi badan saya " + (int)tinggiBadan);  //mengubah nilai double ke int
        System.out.println("Berat badan saya " + (int)beratBadan);  //mengubah nilai double ke int
    }
}