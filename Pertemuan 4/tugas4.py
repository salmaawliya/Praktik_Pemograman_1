i = input("Masukkan nilai i: ")
j = input("Masukkan nilai j: ")

hasil = float(i) + float(j)                      #Ditambahkan type data supaya dapat dieksekusi
print("Nilai i + j = ", + hasil)

hasil = float(i) - float(j)
print("Nilai i - j = ", + hasil)

hasil = float(i) * float(j)
print("Nilai i * j = ", + hasil)

hasil = float(i) / float(j)
print("Nilai i / j = ", + hasil)

hasil = float(i) % float(j)
print("Nilai i % j = ", + hasil)

