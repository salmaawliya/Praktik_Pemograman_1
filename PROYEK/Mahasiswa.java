public class Mahasiswa {
    private String nama;
    private String nim;
    private double ipk;

    public void setNama(String nama) {
        this.nama = nama;
    }
    public String getNama() {
        return this.nama;
    }

    public void setNIM(String nim) {
        this.nim = nim;
    }
    public String getNIM() {
        return this.nim;
    }

    public void setIPK(double ipk) {
        this.ipk = ipk;
    }
    public double getIPK() {
        return this.ipk;
    }
}
